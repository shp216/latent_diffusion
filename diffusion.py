import torch
import torch.nn as nn
import torch.nn.functional as F


def extract(v, t, x_shape):
    """
    Extract some coefficients at specified timesteps, then reshape to
    [batch_size, 1, 1, 1, 1, ...] for broadcasting purposes.
    """
    out = torch.gather(v, index=t, dim=0).float()
    return out.view([t.shape[0]] + [1] * (len(x_shape) - 1))

class distillation_cache_Trainer(nn.Module):
    def __init__(self, T_model, S_model, is_feature, beta_1, beta_T, T,
                 mean_type='eps', var_type='fixedlarge', distill_features = False):
        
        assert mean_type in ['xprev' 'xstart', 'epsilon']
        assert var_type in ['fixedlarge', 'fixedsmall']
        super().__init__()

        self.T_model = T_model  # Teacher model
        self.S_model = S_model  # Student model
        self.T = T
        self.mean_type = mean_type
        self.var_type = var_type
        self.distill_features = distill_features
        self.is_feature = is_feature
        
        self.T_model.eval()

        self.register_buffer(
            'betas', torch.linspace(beta_1, beta_T, T).double())
        alphas = 1. - self.betas
        alphas_bar = torch.cumprod(alphas, dim=0)
        alphas_bar_prev = F.pad(alphas_bar, [1, 0], value=1)[:T]

        # calculations for diffusion q(x_t | x_{t-1}) and others
        self.register_buffer(
            'sqrt_recip_alphas_bar', torch.sqrt(1. / alphas_bar))
        self.register_buffer(
            'sqrt_recipm1_alphas_bar', torch.sqrt(1. / alphas_bar - 1))

        # calculations for posterior q(x_{t-1} | x_t, x_0)
        self.register_buffer(
            'posterior_var',
            self.betas * (1. - alphas_bar_prev) / (1. - alphas_bar))
        # below: log calculation clipped because the posterior variance is 0 at
        # the beginning of the diffusion chain
        self.register_buffer(
            'posterior_log_var_clipped',
            torch.log(
                torch.cat([self.posterior_var[1:2], self.posterior_var[1:]])))
        self.register_buffer(
            'posterior_mean_coef1',
            torch.sqrt(alphas_bar_prev) * self.betas / (1. - alphas_bar))
        self.register_buffer(
            'posterior_mean_coef2',
            torch.sqrt(alphas) * (1. - alphas_bar_prev) / (1. - alphas_bar))
        self.register_buffer(
            'sqrt_alphas_bar', torch.sqrt(alphas_bar))
        self.register_buffer(
            'sqrt_one_minus_alphas_bar', torch.sqrt(1. - alphas_bar))

    def q_mean_variance(self, x_0, x_t, t):
        """
        Compute the mean and variance of the diffusion posterior
        q(x_{t-1} | x_t, x_0)
        """
        assert x_0.shape == x_t.shape
        posterior_mean = (
            extract(self.posterior_mean_coef1, t, x_t.shape) * x_0 +
            extract(self.posterior_mean_coef2, t, x_t.shape) * x_t
        )
        posterior_log_var_clipped = extract(
            self.posterior_log_var_clipped, t, x_t.shape)
        return posterior_mean, posterior_log_var_clipped

    def predict_xstart_from_eps(self, x_t, t, eps):
        assert x_t.shape == eps.shape
        return (
            extract(self.sqrt_recip_alphas_bar, t, x_t.shape) * x_t -
            extract(self.sqrt_recipm1_alphas_bar, t, x_t.shape) * eps
        )

    def teacher_step(self, x_t, t):
        with torch.no_grad():
            teacher_output = self.T_model(x_t, t)

        x_0 = self.predict_xstart_from_eps(x_t, t, eps=teacher_output)
        mean, _ = self.q_mean_variance(x_0, x_t, t)

        model_log_var = {
            # for fixedlarge, we set the initial (log-)variance like so to
            # get a better decoder log likelihood
            'fixedlarge': torch.log(torch.cat([self.posterior_var[1:2],
                                               self.betas[1:]])),
            'fixedsmall': self.posterior_log_var_clipped,
        }[self.var_type]
        log_var = extract(model_log_var, t, x_t.shape)

        noise = torch.randn_like(x_t)
        x_t_ = mean + torch.exp(0.5 * log_var) * noise

        return x_t_
    
    def p_mean_variance_T(self, x_t, t):
        # below: only log_variance is used in the KL computations
        model_log_var = {
            # for fixedlarge, we set the initial (log-)variance like so to
            # get a better decoder log likelihood
            'fixedlarge': torch.log(torch.cat([self.posterior_var[1:2],
                                               self.betas[1:]])),
            'fixedsmall': self.posterior_log_var_clipped,
        }[self.var_type]
        model_log_var = extract(model_log_var, t, x_t.shape)

        # Mean parameterization
        if self.mean_type == 'xprev':       # the model predicts x_{t-1}
            x_prev = self.T_model(x_t, t)
            x_0 = self.predict_xstart_from_xprev(x_t, t, xprev=x_prev)
            model_mean = x_prev
        elif self.mean_type == 'xstart':    # the model predicts x_0
            x_0 = self.T_model(x_t, t)
            model_mean, _ = self.q_mean_variance(x_0, x_t, t)
        elif self.mean_type == 'epsilon':   # the model predicts epsilon
            eps = self.T_model(x_t, t)
            x_0 = self.predict_xstart_from_eps(x_t, t, eps=eps)
            model_mean, _ = self.q_mean_variance(x_0, x_t, t)
        else:
            raise NotImplementedError(self.mean_type)
        x_0 = torch.clip(x_0, -1., 1.)

        return model_mean, model_log_var
    
    def teacher_sampling(self, x_T, i):
        """
        Algorithm 2.
        """
        x_t = x_T
        for time_step in reversed(range(i, self.T)):
            t = x_t.new_ones([x_T.shape[0], ], dtype=torch.long) * time_step
            mean, log_var = self.p_mean_variance_T(x_t=x_t, t=t)
            # no noise when t == 0
            if time_step > 0:
                noise = torch.randn_like(x_t)
            else:
                noise = 0
            x_t = mean + torch.exp(0.5 * log_var) * noise

        return x_t

    def diffusion(self, x_0, t):
        
        noise = torch.randn_like(x_0)
        x_t = (
            extract(self.sqrt_alphas_bar, t, x_0.shape) * x_0 +
            extract(self.sqrt_one_minus_alphas_bar, t, x_0.shape) * noise)
        return x_t
    

    def forward(self, x_t, t):
        """
        Perform the forward pass for knowledge distillation.
        """
        if self.distill_features :
            # Teacher model forward pass (in evaluation mode)
            with torch.no_grad():
                #teacher_output, teacher_features = self.T_model.forward_features(x_t, t)
                teacher_output, teacher_features = self.T_model(x_t, t, is_feature=self.distill_features)

            # Student model forward pass
            #student_output, student_features = self.S_model.forward_features(x_t, t)
            student_output, student_features = self.S_model(x_t, t, is_feature=self.distill_features)
    
            output_loss = F.mse_loss(student_output, teacher_output, reduction='mean')
            
            feature_loss = 0
            for student_feature, teacher_feature in zip(student_features, teacher_features):
                feature_loss += F.mse_loss(student_feature, teacher_feature, reduction='mean')
                
            total_loss = output_loss + feature_loss / len(student_features)
                
        else:
            # Teacher model forward pass (in evaluation mode)
            with torch.no_grad():
                teacher_output = self.T_model(x_t, t)

            # Student model forward pass
            student_output = self.S_model(x_t, t)
            
            output_loss = F.mse_loss(student_output, teacher_output, reduction='mean')
            total_loss = output_loss

        # 여기부터 class-conditional 에서는 CFG로 해야되서 여기 잘 바꿔야함
        x_0 = self.predict_xstart_from_eps(x_t, t, eps=teacher_output)
        mean, _ = self.q_mean_variance(x_0, x_t, t)

        model_log_var = {
            # for fixedlarge, we set the initial (log-)variance like so to
            # get a better decoder log likelihood
            'fixedlarge': torch.log(torch.cat([self.posterior_var[1:2],
                                               self.betas[1:]])),
            'fixedsmall': self.posterior_log_var_clipped,
        }[self.var_type]
        log_var = extract(model_log_var, t, x_t.shape)

        noise = torch.randn_like(x_t)
        x_t = mean + torch.exp(0.5 * log_var) * noise 

        

        return output_loss, x_t, total_loss
    

class GaussianDiffusion_distillation_Trainer(nn.Module):
    def __init__(self, T_model, S_model, beta_1, beta_T, T):
        super().__init__()

        self.T_model = T_model  # Teacher model
        self.S_model = S_model  # Student model
        self.T = T
        
        self.T_model.eval()
        
        self.register_buffer(
            'betas', torch.linspace(beta_1, beta_T, T).double())
        alphas = 1. - self.betas
        alphas_bar = torch.cumprod(alphas, dim=0)

        # Calculations for diffusion q(x_t | x_{t-1}) and others
        self.register_buffer(
            'sqrt_alphas_bar', torch.sqrt(alphas_bar))
        self.register_buffer(
            'sqrt_one_minus_alphas_bar', torch.sqrt(1. - alphas_bar))

    def forward(self, x_t):
        """
        Perform the forward pass for knowledge distillation.
        """
        
        t = torch.full((x_t.shape[0],), self.T-1, device=x_t.device)
        
        # Teacher model forward pass (in evaluation mode)
        with torch.no_grad():
            teacher_output = self.T_model(x_t, t)

        # Student model forward pass
        student_output = self.S_model(x_t, t)

        # Calculate the MSE loss between teacher and student outputs
        loss = F.mse_loss(student_output, teacher_output, reduction='mean')

        return loss
    
